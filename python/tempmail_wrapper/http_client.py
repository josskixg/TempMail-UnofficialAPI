class HttpClient:
    """HTTP client with UA rotation, proxy rotation, retry/backoff, and cookie support."""

    def __init__(
        self,
        proxies: list[str] | None = None,
        random_ua: bool = True,
        use_cookies: bool = True,
    ):
        self.proxies = proxies or []
        self.random_ua = random_ua
        self.session = requests.Session() if use_cookies else None
        self._proxy_idx = 0

    def _get_proxy(self) -> dict[str, str] | None:
        if not self.proxies:
            return None
        proxy = self.proxies[self._proxy_idx % len(self.proxies)]
        self._proxy_idx += 1
        return {"http": proxy, "https": proxy}

    def _get_ua(self) -> str:
        return random.choice(UA_POOL) if self.random_ua else UA_POOL[0]

    def request(self, method: str, url: str, **kwargs) -> requests.Response:
        """Execute request with retry, proxy rotation, UA rotation, and direct fallback."""
        delays = [1, 3, 5]
        last_err: Exception | None = None

        for attempt in range(len(delays) + 1):
            # Rotate UA each attempt
            headers = kwargs.pop("headers", {}) or {}
            if "User-Agent" not in headers:
                headers["User-Agent"] = self._get_ua()
            kwargs["headers"] = headers

            # Try with proxy (if available)
            proxy = self._get_proxy()
            if proxy:
                kwargs["proxies"] = proxy

            try:
                if self.session:
                    resp = self.session.request(method, url, **kwargs)
                else:
                    resp = requests.request(method, url, **kwargs)
                return resp
            except (requests.ConnectionError, requests.Timeout, requests.ProxyError) as e:
                last_err = e
                if attempt < len(delays):
                    time.sleep(delays[attempt])
                continue

        # All proxy attempts failed — fallback to direct connection
        if self.proxies and last_err:
            for attempt in range(len(delays) + 1):
                headers = kwargs.pop("headers", {}) or {}
                if "User-Agent" not in headers:
                    headers["User-Agent"] = self._get_ua()
                kwargs["headers"] = headers
                kwargs.pop("proxies", None)  # Remove proxy for direct connection

                try:
                    if self.session:
                        resp = self.session.request(method, url, **kwargs)
                    else:
                        resp = requests.request(method, url, **kwargs)
                    return resp
                except (requests.ConnectionError, requests.Timeout) as e:
                    last_err = e
                    if attempt < len(delays):
                        time.sleep(delays[attempt])

        raise last_err or requests.RequestException("All attempts failed")

    def get(self, url: str, **kwargs) -> requests.Response:
        return self.request("GET", url, **kwargs)

    def post(self, url: str, **kwargs) -> requests.Response:
        return self.request("POST", url, **kwargs)
